    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if(employeeTable.getSelectedRow() != -1){
            int ok = JOptionPane.showConfirmDialog(null, "Esti sigur ? Operatiunea este ireversibila !",null,JOptionPane.YES_NO_OPTION);
            if(ok == JOptionPane.YES_OPTION){
                String Id = String.valueOf((Integer) employeeTable.getValueAt(employeeTable.getSelectedRow(), 0));
                Employee emp = Employee.findBy("Id", Id);
                if (emp.delete()) {
                    clearTable();
                    populateTable();
                } else {
                    JOptionPane.showMessageDialog(null, "Nu s-a putut sterge", null, JOptionPane.ERROR_MESSAGE, null);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Trebuie selectat un rand din tabel", null, JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

