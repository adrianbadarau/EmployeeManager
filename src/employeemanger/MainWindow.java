    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) employeeTable.getModel();
        if (employeeTable.getSelectedRow() != -1){
            String id = String.valueOf((Integer) employeeTable.getValueAt(employeeTable.getSelectedRow(), 0));
            System.out.println(id);
            Employee emp = Employee.findBy("Id", id);
            editFrame.setBounds(100, 100, 300, 300);
            editFrame.setVisible(true);
            editNameTxtField.setText(emp.getName());
            editAgeTxtField.setText(String.valueOf(emp.getAge()));
            editAddressTxtField.setText(emp.getAddress());
            editSalaryTxtField.setText(String.valueOf(emp.getSalary()));
            editSaveButton.addActionListener(new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    emp.setName(editNameTxtField.getText().toString());
                    emp.setAddress(editAddressTxtField.getText().toString());
                    emp.setAge(Integer.parseInt(editAgeTxtField.getText().toString()));
                    emp.setSalary(Float.parseFloat(editSalaryTxtField.getText().toString()));
                    emp.update();
                    clearEditForm();
                    editFrame.setVisible(false);
                    clearTable();
                    populateTable();
                }
            });
        }else{
            JOptionPane.showMessageDialog(null, "Trebuie sa selectezi un rand din tabel");
        }
    }//GEN-LAST:event_editButtonActionPerformed

