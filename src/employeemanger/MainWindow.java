    public void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) employeeTable.getModel();
        ArrayList<Employee> employees = Employee.findAll();
        for(Employee emp : employees){
            model.addRow(new Object[]{emp.getId(),emp.getName(),emp.getAge(),emp.getAddress(),emp.getSalary()});
        }
    }
    
    public void populateTable(ArrayList<Employee> employees)
    {
        DefaultTableModel model = (DefaultTableModel) employeeTable.getModel();
        for(Employee emp : employees){
            model.addRow(new Object[]{emp.getId(),emp.getName(),emp.getAge(),emp.getAddress(),emp.getSalary()});
        }
    }
    
    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Employee emp = new Employee();
        emp.setName(nameField.getText());
        emp.setAddress(addressField.getText());
        emp.setSalary(Float.parseFloat(salaryField.getText()));
        emp.setAge(Integer.parseInt(ageField.getText()));
        emp.save();
        JOptionPane.showMessageDialog(null, "Angajatul a fost Salvat cu succes");
        clearInputs();
    }//GEN-LAST:event_saveButtonActionPerformed

